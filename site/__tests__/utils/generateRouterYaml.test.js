import { describe, it, expect } from 'vitest';
import { generateRouterYaml, getRequiredEnvVars } from '../../src/utils/generateRouterYaml';

describe('generateRouterYaml', () => {
  const baseProviders = [
    { id: 'p1', name: 'OpenAI', provider_type: 'openai', base_url: 'https://api.openai.com/v1', enabled: true },
    { id: 'p2', name: 'Local vLLM', provider_type: 'vllm', base_url: 'http://127.0.0.1:8000/v1', enabled: true },
  ];

  const baseModels = [
    { id: 'm1', name: 'gpt-4o', provider_model_id: 'gpt-4o', api_format: 'openai', reasoning_family: null, pricing_prompt_per_1m: 2.5, pricing_completion_per_1m: 10, capabilities: ['chat', 'tools', 'vision'], is_default: true },
    { id: 'm2', name: 'local-qwen', provider_model_id: 'qwen3-8b', api_format: 'openai', reasoning_family: 'qwen3', pricing_prompt_per_1m: 0, pricing_completion_per_1m: 0, capabilities: ['chat', 'reasoning'], is_default: false },
  ];

  const baseBackends = [
    { model_id: 'm1', provider_id: 'p1', weight: 100, is_fallback: false },
    { model_id: 'm2', provider_id: 'p2', weight: 80, is_fallback: false },
  ];

  const baseDecisions = [
    {
      name: 'math_decision', description: 'Math queries', priority: 10, enabled: true,
      rules: { operator: 'OR', conditions: [{ type: 'domain', name: 'math' }] },
      model_refs: [{ model: 'm2', use_reasoning: true }],
      plugins: [{ type: 'system_prompt', configuration: { enabled: true, system_prompt: 'You are a math expert.', mode: 'replace' } }],
    },
  ];

  it('generates valid YAML with header comments', () => {
    const yaml = generateRouterYaml({ providers: [], models: [], backends: [], decisions: [] });
    expect(yaml).toContain('# vLLM Semantic Router Configuration');
    expect(yaml).toContain('# Generated by Slancha Dashboard');
    expect(yaml).toContain('listeners:');
  });

  it('generates listeners section with defaults', () => {
    const yaml = generateRouterYaml({ providers: [], models: [], backends: [], decisions: [] });
    expect(yaml).toContain('  - name: default');
    expect(yaml).toContain('    address: 0.0.0.0');
    expect(yaml).toContain('    port: 8080');
  });

  it('generates providers.defaults section', () => {
    const yaml = generateRouterYaml({ providers: [], models: [], backends: [], decisions: [] });
    expect(yaml).toContain('providers:');
    expect(yaml).toContain('  defaults:');
    expect(yaml).toContain('    max_retries: 3');
  });

  it('generates model entries with pricing and capabilities', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: [], decisions: [] });
    expect(yaml).toContain('    - name: gpt-4o');
    expect(yaml).toContain('      provider_model_id: gpt-4o');
    expect(yaml).toContain('      api_format: openai');
    expect(yaml).toContain('      pricing:');
    expect(yaml).toContain('        prompt_per_1m: 2.5');
    expect(yaml).toContain('        completion_per_1m: 10');
    expect(yaml).toContain('      capabilities: [chat, tools, vision]');
    expect(yaml).toContain('      is_default: true');
  });

  it('generates reasoning_family when present', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: [], decisions: [] });
    expect(yaml).toContain('      reasoning_family: qwen3');
  });

  it('omits reasoning_family when null', () => {
    const lines = generateRouterYaml({ providers: baseProviders, models: [baseModels[0]], backends: [], decisions: [] }).split('\n');
    const gptBlock = lines.slice(lines.indexOf('    - name: gpt-4o'));
    const hasReasoning = gptBlock.some(l => l.includes('reasoning_family'));
    expect(hasReasoning).toBe(false);
  });

  it('uses base_url for remote providers and endpoint for vllm', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: [] });
    // OpenAI gets base_url + api_key_env (URLs get quoted in YAML)
    expect(yaml).toContain('base_url: "https://api.openai.com/v1"');
    expect(yaml).toContain('api_key_env: SLANCHA_OPENAI_API_KEY');
    // vLLM gets endpoint, no api_key_env
    expect(yaml).toContain('endpoint: "http://127.0.0.1:8000/v1"');
  });

  it('does not include api_key_env for vllm providers', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: [baseModels[1]], backends: [baseBackends[1]], decisions: [] });
    const lines = yaml.split('\n');
    // Find the vllm backend block
    const endpointLine = lines.findIndex(l => l.includes('endpoint: "http://127.0.0.1:8000'));
    // The next line should NOT be api_key_env
    expect(lines[endpointLine + 1]).not.toContain('api_key_env');
  });

  it('generates backend_refs with weight and fallback', () => {
    const backendsWithFallback = [
      { model_id: 'm1', provider_id: 'p1', weight: 80, is_fallback: false },
      { model_id: 'm1', provider_id: 'p2', weight: 20, is_fallback: true },
    ];
    const yaml = generateRouterYaml({ providers: baseProviders, models: [baseModels[0]], backends: backendsWithFallback, decisions: [] });
    expect(yaml).toContain('          weight: 80');
    expect(yaml).toContain('          weight: 20');
    expect(yaml).toContain('          is_fallback: true');
  });

  it('generates routing decisions with rules and conditions', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: baseDecisions });
    expect(yaml).toContain('routing:');
    expect(yaml).toContain('  decisions:');
    expect(yaml).toContain('    - name: math_decision');
    expect(yaml).toContain('      description: Math queries');
    expect(yaml).toContain('      priority: 10');
    expect(yaml).toContain('        operator: OR');
    expect(yaml).toContain('          - type: domain');
    expect(yaml).toContain('            name: math');
  });

  it('resolves model_refs by ID to model name', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: baseDecisions });
    // model_refs has model: 'm2', which should resolve to 'local-qwen'
    expect(yaml).toContain('        - model: local-qwen');
    expect(yaml).toContain('          use_reasoning: true');
  });

  it('generates plugins with configuration', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: baseDecisions });
    expect(yaml).toContain('      plugins:');
    expect(yaml).toContain('        - type: system_prompt');
    expect(yaml).toContain('          configuration:');
    expect(yaml).toContain('            enabled: true');
    expect(yaml).toContain('            mode: replace');
  });

  it('skips disabled decisions', () => {
    const disabledDecisions = [{ ...baseDecisions[0], enabled: false }];
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: disabledDecisions });
    expect(yaml).not.toContain('math_decision');
  });

  it('omits routing section when no decisions', () => {
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: baseBackends, decisions: [] });
    expect(yaml).not.toContain('routing:');
  });

  it('handles empty inputs gracefully', () => {
    const yaml = generateRouterYaml({});
    expect(yaml).toContain('listeners:');
    expect(yaml).toContain('providers:');
    expect(yaml).toContain('  models:');
    expect(yaml).not.toContain('routing:');
  });

  it('escapes special YAML characters in strings', () => {
    const models = [{ id: 'm1', name: 'model: with colon', provider_model_id: 'test#hash', capabilities: [], is_default: false }];
    const yaml = generateRouterYaml({ providers: [], models, backends: [], decisions: [] });
    expect(yaml).toContain('"model: with colon"');
    expect(yaml).toContain('"test#hash"');
  });

  it('skips backend_refs for providers that do not exist', () => {
    const orphanBackends = [{ model_id: 'm1', provider_id: 'nonexistent', weight: 100, is_fallback: false }];
    const yaml = generateRouterYaml({ providers: baseProviders, models: baseModels, backends: orphanBackends, decisions: [] });
    // Should have backend_refs header but no entries under it (provider not found → skipped)
    expect(yaml).not.toContain('          provider: nonexistent');
  });
});

describe('getRequiredEnvVars', () => {
  it('returns env vars for enabled non-vllm providers', () => {
    const providers = [
      { provider_type: 'openai', enabled: true },
      { provider_type: 'anthropic', enabled: true },
      { provider_type: 'vllm', enabled: true },
      { provider_type: 'mistral', enabled: false },
    ];
    const vars = getRequiredEnvVars(providers);
    expect(vars).toEqual(['SLANCHA_OPENAI_API_KEY', 'SLANCHA_ANTHROPIC_API_KEY']);
  });

  it('returns empty array when no providers need keys', () => {
    const providers = [{ provider_type: 'vllm', enabled: true }];
    expect(getRequiredEnvVars(providers)).toEqual([]);
  });

  it('returns empty array for empty input', () => {
    expect(getRequiredEnvVars([])).toEqual([]);
  });
});
