/**
 * Pure function: generates valid vLLM Semantic Router config YAML.
 * Maps providers, models, backends, and decisions into the schema from
 * github.com/vllm-project/semantic-router/blob/main/config/config.yaml
 *
 * API keys are referenced as env vars (SLANCHA_{PROVIDER_TYPE}_API_KEY),
 * never embedded in the YAML output.
 */

function indent(str, level) {
  const pad = '  '.repeat(level);
  return str.split('\n').map(line => pad + line).join('\n');
}

function yamlString(val) {
  if (val == null) return '""';
  const s = String(val);
  if (s === '' || /[:#{}[\],&*?|>!%@`]/.test(s) || s.includes('\n') || s.startsWith(' ') || s.endsWith(' ')) {
    return `"${s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
  }
  return s;
}

function envVarName(providerType) {
  return `SLANCHA_${(providerType || 'CUSTOM').toUpperCase()}_API_KEY`;
}

export function generateRouterYaml({ providers = [], models = [], backends = [], decisions = [] }) {
  const lines = [];

  // Header
  lines.push('# vLLM Semantic Router Configuration');
  lines.push('# Generated by Slancha Dashboard');
  lines.push(`# ${new Date().toISOString()}`);
  lines.push('');

  // Listeners
  lines.push('listeners:');
  lines.push('  - name: default');
  lines.push('    address: 0.0.0.0');
  lines.push('    port: 8080');
  lines.push('');

  // Providers
  lines.push('providers:');

  // Defaults
  lines.push('  defaults:');
  lines.push('    max_retries: 3');
  lines.push('    timeout: 30');
  lines.push('');

  // Build provider lookup
  const providerMap = {};
  providers.forEach(p => { providerMap[p.id] = p; });

  // Build model lookup
  const modelMap = {};
  models.forEach(m => { modelMap[m.id] = m; });

  // Models section
  lines.push('  models:');

  const enabledProviders = providers.filter(p => p.enabled);

  models.forEach(model => {
    const modelBackends = backends.filter(b => b.model_id === model.id);
    lines.push(`    - name: ${yamlString(model.name)}`);
    lines.push(`      provider_model_id: ${yamlString(model.provider_model_id || model.name)}`);

    if (model.api_format) {
      lines.push(`      api_format: ${yamlString(model.api_format)}`);
    }
    if (model.reasoning_family) {
      lines.push(`      reasoning_family: ${yamlString(model.reasoning_family)}`);
    }

    // Pricing
    if (model.pricing_prompt_per_1m != null || model.pricing_completion_per_1m != null) {
      lines.push('      pricing:');
      if (model.pricing_prompt_per_1m != null) {
        lines.push(`        prompt_per_1m: ${model.pricing_prompt_per_1m}`);
      }
      if (model.pricing_completion_per_1m != null) {
        lines.push(`        completion_per_1m: ${model.pricing_completion_per_1m}`);
      }
    }

    // Capabilities
    if (model.capabilities && model.capabilities.length > 0) {
      lines.push(`      capabilities: [${model.capabilities.map(c => yamlString(c)).join(', ')}]`);
    }

    if (model.is_default) {
      lines.push('      is_default: true');
    }

    // Backend refs
    if (modelBackends.length > 0) {
      lines.push('      backend_refs:');
      modelBackends.forEach(backend => {
        const provider = providerMap[backend.provider_id];
        if (!provider) return;

        lines.push(`        - provider: ${yamlString(provider.name)}`);

        if (provider.provider_type === 'vllm') {
          lines.push(`          endpoint: ${yamlString(provider.base_url)}`);
        } else {
          lines.push(`          base_url: ${yamlString(provider.base_url)}`);
          lines.push(`          api_key_env: ${envVarName(provider.provider_type)}`);
        }

        lines.push(`          weight: ${backend.weight}`);
        if (backend.is_fallback) {
          lines.push('          is_fallback: true');
        }
      });
    }

    lines.push('');
  });

  // Routing decisions
  if (decisions.length > 0) {
    lines.push('routing:');
    lines.push('  decisions:');

    decisions.filter(d => d.enabled).forEach(decision => {
      lines.push(`    - name: ${yamlString(decision.name)}`);
      if (decision.description) {
        lines.push(`      description: ${yamlString(decision.description)}`);
      }
      lines.push(`      priority: ${decision.priority}`);

      // Rules
      if (decision.rules) {
        lines.push('      rules:');
        lines.push(`        operator: ${decision.rules.operator || 'OR'}`);
        if (decision.rules.conditions && decision.rules.conditions.length > 0) {
          lines.push('        conditions:');
          decision.rules.conditions.forEach(cond => {
            lines.push(`          - type: ${yamlString(cond.type)}`);
            lines.push(`            name: ${yamlString(cond.name)}`);
          });
        }
      }

      // Model refs
      if (decision.model_refs && decision.model_refs.length > 0) {
        lines.push('      modelRefs:');
        decision.model_refs.forEach(ref => {
          const refModel = modelMap[ref.model];
          const modelName = refModel ? refModel.name : ref.model;
          lines.push(`        - model: ${yamlString(modelName)}`);
          if (ref.lora_name) {
            lines.push(`          lora_name: ${yamlString(ref.lora_name)}`);
          }
          if (ref.use_reasoning) {
            lines.push('          use_reasoning: true');
          }
        });
      }

      // Plugins
      if (decision.plugins && decision.plugins.length > 0) {
        lines.push('      plugins:');
        decision.plugins.forEach(plugin => {
          lines.push(`        - type: ${yamlString(plugin.type)}`);
          if (plugin.configuration) {
            lines.push('          configuration:');
            Object.entries(plugin.configuration).forEach(([key, val]) => {
              lines.push(`            ${key}: ${yamlString(val)}`);
            });
          }
        });
      }

      lines.push('');
    });
  }

  return lines.join('\n');
}

/**
 * Returns list of required env var names based on enabled providers.
 */
export function getRequiredEnvVars(providers) {
  return providers
    .filter(p => p.enabled && p.provider_type !== 'vllm')
    .map(p => envVarName(p.provider_type));
}

export default generateRouterYaml;
